USE AmazonDB;
-- 1) List all customers who have made purchases of more than $80.
SELECT -- It will select columns to show in output
    orders.user_id,
    SUM(orders.total_amount) AS total_spent,
    Users.name
FROM
    orders
INNER JOIN
    Users
ON 
    orders.user_id = Users.user_id
GROUP BY
    orders.user_id
HAVING
    SUM(orders.total_amount) > 80; -- we grouped by user_id before sum. It adds amounts for each user as group. If not for group, then we can use WHERE itself.
    
-- 2) Retrieve all orders placed in the last 280 days along with the customer name and email.
SELECT
	orders.user_id,
    orders.order_date,
    Users.name,
    Users.email
FROM
	orders
INNER JOIN
	Users
ON
	orders.user_id = Users.user_id
WHERE orders.order_date >= CURRENT_DATE - INTERVAL 280 DAY;

-- 3.Find the average product price for each category.
SELECT
	products.category,
	AVG(products.price) AS average_price
FROM  products
GROUP BY products.category;

-- 4. List all customers who have purchased a product from the category Electronics.
SELECT DISTINCT -- DISTINCT WILL SELECT UNIQUE VALUES BY REMOVING DUPLICATES
	Users.name,
    Users.email
FROM
	Users
INNER JOIN
	orders ON Users.user_id = orders.user_id
INNER JOIN
	OrderDetails ON orders.order_id = OrderDetails.order_id
INNER JOIN   
	products ON OrderDetails.product_id = products.product_id
WHERE products.category = 'Electronics';

-- 5.Find the total number of products sold and the total revenue generated for each product.
SELECT
	products.product_id,
    products.name,
    SUM(OrderDetails.quantity) AS no_of_products_sold,
    SUM(OrderDetails.quantity * products.price) AS total_revenue
FROM
	OrderDetails
INNER JOIN 
	products ON OrderDetails.product_id = products.product_id
GROUP BY OrderDetails.product_id;
	
