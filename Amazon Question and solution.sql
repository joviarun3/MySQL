-- 11.List all customers who purchased more than 1 units of any product, including the product name and total quantity purchased.
SELECT
	Users.user_id,
    Users.name AS Customer_name,
    products.name AS Product_name,
    SUM(OrderDetails.quantity) AS total_quantity
FROM
	Users
INNER JOIN
	Orders ON Users.user_id = Orders.user_id
INNER JOIN
	OrderDetails ON Orders.order_id = OrderDetails.order_id
INNER JOIN 
	products ON OrderDetails.product_id = products.product_id
    
GROUP BY Users.user_id,products.product_id    
HAVING SUM(OrderDetails.quantity) > 1;

